"""
compare_counterfactual_results.py
==================================

Reads the original, aggressive-pruned, and counterfactual test-set metrics
and emits the headline 3x3-per-degraded-experiment comparison the
COUNTERFACTUAL_PLAN.md spec requires, plus the over-cautiousness summary
on the 4 healthy-after-pruning probes.

Inputs:
    - Original    : compound_flooding/MLMiami FFCA Prunned Results/results/ffca_vs_original_table3.csv
                    (already aggregates 'Original' + 'FFCA' (aggressive-pruned) per experiment)
    - Counterfactual: FFCA_agent/case_studies/counterfactual_runs/<exp>/<variant>/MLP/test/results.csv

Outputs:
    - FFCA_agent/case_studies/counterfactual_runs/RESULTS.md
    - FFCA_agent/case_studies/counterfactual_runs/RESULTS_summary.csv
"""
import json
from pathlib import Path

import pandas as pd

REPO_ROOT     = Path(__file__).resolve().parents[2]
COUNTERFACT   = REPO_ROOT / 'FFCA_agent' / 'case_studies' / 'counterfactual_runs'
ORIGINAL_CSV  = REPO_ROOT / 'compound_flooding' / 'MLMiami FFCA Prunned Results' / 'results' / 'ffca_vs_original_table3.csv'

# (experiment_name, category_label, lead_time, displayed_label)
DEGRADED = [
    ('12hr_perfect_prog_gate_sigmoid',       'Predicted Gate Opening',     12, 'Gate 12h'),
    ('24hr_perfect_prog_gate_sigmoid',       'Predicted Gate Opening',     24, 'Gate 24h'),
    ('24hr_perfect_prog_all_inputs_sigmoid', 'Predictions All Inputs',     24, 'AllInputs 24h'),
]
OK_PROBES = [
    ('24hr_measured_sigmoid',                'Measurements Only',          24, 'Measured 24h'),
    ('24hr_perfect_prog_wls_sigmoid',        'Predicted Ocean Water Levels', 24, 'WLS 24h'),
    ('12hr_perfect_prog_rain_sigmoid',       'Predicted Rainfall',         12, 'Rain 12h'),
    ('12hr_perfect_prog_all_inputs_sigmoid', 'Predictions All Inputs',     12, 'AllInputs 12h'),
]

VARIANTS = ['variant_A_patience100', 'variant_B_lr_schedule']


def read_original_table():
    df = pd.read_csv(ORIGINAL_CSV)
    return df


def lookup_original(df, category, lead, variant):
    """variant = 'Original' or 'FFCA' (aggressive-pruned)."""
    row = df[(df['Model'] == category) & (df['Lead (h)'] == lead) & (df['Variant'] == variant)]
    if row.empty:
        return None
    r = row.iloc[0]
    return dict(RMSE_cm=float(r['RMSE_cm']), R2=float(r['R2']), N_feat=int(r['N_feat']))


def read_counterfactual(exp_name, variant_name):
    p = COUNTERFACT / exp_name / variant_name / 'MLP' / 'test' / 'results.csv'
    if not p.exists():
        return None
    df = pd.read_csv(p, index_col=0)
    r = df.iloc[0]
    return dict(RMSE_cm=float(r['RMSE']) * 100, R2=float(r['R2']), N_feat=int(r['n_features']))


def main():
    df_orig = read_original_table()
    rows = []
    headline_lines = ['# Counterfactual results — headline tables', '', '_Generated by compare_counterfactual_results.py._', '']

    headline_lines.append('## Degraded after aggressive pruning (n=3)')
    headline_lines.append('')
    headline_lines.append('Each row asks: did the agent\'s recommended path beat aggressive pruning?')
    headline_lines.append('')
    headline_lines.append('| Experiment | Original (no prune) | Aggressive-prune | Variant A (loose patience) | Variant B (A + LR sched) |')
    headline_lines.append('|---|:--:|:--:|:--:|:--:|')

    for exp, cat, lead, label in DEGRADED:
        orig = lookup_original(df_orig, cat, lead, 'Original') or {}
        prune = lookup_original(df_orig, cat, lead, 'FFCA') or {}
        cf_a = read_counterfactual(exp, 'variant_A_patience100') or {}
        cf_b = read_counterfactual(exp, 'variant_B_lr_schedule') or {}

        def cell(d):
            if not d: return 'pending'
            return f'{d["RMSE_cm"]:.2f} cm / R²={d["R2"]:.2f}'

        headline_lines.append(f'| **{label}** | {cell(orig)} | {cell(prune)} | {cell(cf_a)} | {cell(cf_b)} |')

        rows.append(dict(experiment=exp, label=label, kind='degraded',
                         orig_rmse=orig.get('RMSE_cm'), orig_r2=orig.get('R2'),
                         prune_rmse=prune.get('RMSE_cm'), prune_r2=prune.get('R2'),
                         cfA_rmse=cf_a.get('RMSE_cm'), cfA_r2=cf_a.get('R2'),
                         cfB_rmse=cf_b.get('RMSE_cm'), cfB_r2=cf_b.get('R2')))

    headline_lines.append('')
    headline_lines.append('### Verdict (auto-derived)')
    headline_lines.append('')
    wins_A = 0
    wins_B = 0
    for r in rows:
        if r['kind'] != 'degraded':
            continue
        if r['cfA_rmse'] is not None and r['prune_rmse'] is not None:
            if r['cfA_rmse'] < r['prune_rmse']:
                wins_A += 1
        if r['cfB_rmse'] is not None and r['prune_rmse'] is not None:
            if r['cfB_rmse'] < r['prune_rmse']:
                wins_B += 1
    headline_lines.append(f'- Variant A beats aggressive-prune on **{wins_A} of 3** degraded experiments.')
    headline_lines.append(f'- Variant B beats aggressive-prune on **{wins_B} of 3** degraded experiments.')

    # Over-cautiousness probe
    headline_lines.append('')
    headline_lines.append('## Over-cautiousness probe — 4 healthy-after-pruning experiments')
    headline_lines.append('')
    headline_lines.append('Did the agent\'s uniform "train longer" call hurt models that were actually fine?')
    headline_lines.append('')
    headline_lines.append('| Experiment | Original (no prune) | Aggressive-prune | Variant A |')
    headline_lines.append('|---|:--:|:--:|:--:|')
    for exp, cat, lead, label in OK_PROBES:
        orig = lookup_original(df_orig, cat, lead, 'Original') or {}
        prune = lookup_original(df_orig, cat, lead, 'FFCA') or {}
        cf_a = read_counterfactual(exp, 'variant_A_patience100') or {}

        def cell(d):
            if not d: return 'pending'
            return f'{d["RMSE_cm"]:.2f} cm / R²={d["R2"]:.2f}'

        headline_lines.append(f'| **{label}** | {cell(orig)} | {cell(prune)} | {cell(cf_a)} |')
        rows.append(dict(experiment=exp, label=label, kind='ok_probe',
                         orig_rmse=orig.get('RMSE_cm'), orig_r2=orig.get('R2'),
                         prune_rmse=prune.get('RMSE_cm'), prune_r2=prune.get('R2'),
                         cfA_rmse=cf_a.get('RMSE_cm'), cfA_r2=cf_a.get('R2'),
                         cfB_rmse=None, cfB_r2=None))

    out_md = COUNTERFACT / 'RESULTS.md'
    out_csv = COUNTERFACT / 'RESULTS_summary.csv'
    out_md.write_text('\n'.join(headline_lines) + '\n')
    pd.DataFrame(rows).to_csv(out_csv, index=False)
    print(f'Wrote {out_md}')
    print(f'Wrote {out_csv}')
    print()
    print('\n'.join(headline_lines))


if __name__ == '__main__':
    main()
